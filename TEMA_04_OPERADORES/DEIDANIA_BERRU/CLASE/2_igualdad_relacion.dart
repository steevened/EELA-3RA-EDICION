// Igualdad - relacion
void main() {
final a = 5;
final b = 10;
// Operacion de igualdad
// a==b
// 5==10
final igualdad = a ==b ;
print("Igualdad:");
print(igualdad);

// Operacion no es igual
// a != b
// 5 != 10
final desigualdad = a != b ;
print("No es igual:");
print(desigualdad);

// Mayor que
// a > b
// 5 > 10
final mayorQue = a > b ;
print("Mayor que :");
print(mayorQue);

// menor que
// a < b
// 5 < 10
final menorQue = a < b ;
print("Menor que :");
print(menorQue);

// Mayor o igual
// a >= b
// 5 >= 10
final mayorOIgual = a >= b ;
print("Mayor o Igual :");
print(mayorOIgual);

// Menor o igual
// a <= b
// 5 <= 10
final menorOIgual = a <= b ;
print("Menor o Igual :");
print(menorOIgual);





}