
void main() { 
  // Suma + 
  final suma = 10 + 20;
  print(suma);

  //Resta -
  final resta = 20 - 10;
  print(resta);

  //Multiplicacion *
  final multiplicacion = 10 * 20;
  print(multiplicacion);

  //Division /
  final division = 20 / 10;
  print(division);

  //Modulo %
  final modulo = 20 % 10;
  print(modulo);
  
  print("-------------------");
  //Incremento ++
  int incremento = 1;
  int incremento2 = 1;
  
  print(incremento++);
  print(incremento);

  print(++incremento2);
  print(incremento2);

  print("-------------------");
  //Incremento ++
  int decremento = 1;
  int decremento2 = 1;
  
  print(decremento--);
  print(decremento);

  print(--decremento2);
  print(decremento2);
}