
void main() {
  // igualdad 
  final a = 10;
  final b = 20;

  final igualdad = a == b;
  print("El numero $a es igual a $b? Res: $igualdad");

  // desigualdad
  final desigualdad = a != b;
  print("El numero $a es diferente a $b? Res: $desigualdad");

  // mayor que >
  final mayorA = a > b;
  print("El numero $a es mayor a $b? Res: $mayorA");

  // menor que <
  final menorA = a < b;
  print("El numero $a es menor a $b? Res: $menorA");

  // mayor o igual que >=
  final mayorIgualA = a >= b;
  print("El numero $a es mayor o igual a $b? Res: $mayorIgualA");

  // menor o igual que <=
  final menorIgualA = a <= b;
  print("El numero $a es menor o igual a $b? Res: $menorIgualA");


}