/// 
/// El lenguaje de programación Dart fue desarrollado por Google y su nombre fue elegido como un juego de palabras. 
/// "Dart" en inglés significa "dardo", que es un objeto pequeño y ligero diseñado para ser lanzado con precisión. 
/// El nombre refleja la intención de Google de crear un lenguaje de programación que sea rápido, eficiente y preciso, 
/// permitiendo a los desarrolladores construir aplicaciones con alto rendimiento y exactitud.

