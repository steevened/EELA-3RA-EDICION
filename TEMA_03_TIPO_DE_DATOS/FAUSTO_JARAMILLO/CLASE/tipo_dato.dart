void main() {
    // final
    // final String --> solo texto
    final String = "Es un string";
    print(String);

    // final num --> números abstractos
    final  num = "Es un número";
    print(num);

    // const
    // int --> números enteros
    int pi = 5;
    print(pi);

    // double --> números con decimales
    double pi2 = 3.3;
    print(pi2);
    
    // var
    // declarar variables
    var balance = 20.20;
    print(balance);

    var balance2 = 10.1;
    print(balance2);
    
}